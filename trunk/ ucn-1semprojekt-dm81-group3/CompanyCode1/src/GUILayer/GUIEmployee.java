    private void insertEmp(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertEmp
        CtrEmp ctrEmp = new CtrEmp();
  
        String fname = jTextField1.getText();
        String lname = jTextField2.getText();
        double salary = Double.parseDouble(inSalary.getText());
        String superviserName = inSuperviserName.getText();
        Employee superviser = checkSupervisor(superviserName);
        if(superviser != null){
             ctrEmp.insertNew(fname, lname, salary, superviser.getSsn());
        }
        else{
            jLabel9.setText("Superviser it not in the system");
        }
    }//GEN-LAST:event_insertEmp

    private void findProjectInformation(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findProjectInformation
          CtrEmp ctrEmp = new CtrEmp();
          String ssn = jTextField3.getText();
          if (ssn.length() == 0){
              //write error
              jLabel9.setText("Please insert ssn on the employee");
          }
          else {
              ArrayList<WorksOn> empProject = ctrEmp.findProject(ssn);
              buildList(empProject);
          }
    }//GEN-LAST:event_findProjectInformation

    private void updateEmployee(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateEmployee
     
           //read the new values from the screen and update them in the database
           CtrEmp ctrEmp = new CtrEmp();
           String fname = jTextField1.getText();
           System.out.println("+" + fname + "+");
           String lname = jTextField2.getText();
           String ssn = jTextField3.getText();
           String superFname = inSuperviserName.getText();
           Employee superEmp = checkSupervisor(superFname);
           if(superEmp != null){
               double salary = Double.parseDouble(inSalary.getText());
               int result = ctrEmp.updateEmp(fname,lname,ssn,salary, superEmp.getSsn());
               if(result == 1){
                clearFields();}
               else{
                  jLabel9.setText("Problem with update");
               }
            }
               else{
                   jLabel9.setText("The supervisor is not correct ");
               }   
          
        
           
    }//GEN-LAST:event_updateEmployee
