    private void putValuesOnTheScreen(Employee empObj)
    {
          DecimalFormat fmt = new DecimalFormat("####.##");
          jTextField1.setText(empObj.getFname());
          jTextField2.setText(empObj.getLname());
          jTextField3.setText(empObj.getSsn());
          inSalary.setText(String.valueOf(fmt.format(empObj.getSalary())));
          inSuperviserName.setText(empObj.getSupervisor().getFname());
          //System.out.println("empObj" + empObj.getDept().getDname());
          //put department on the screen
           jTextField6.setText(empObj.getDept().getDname());
         
             
    }
    private void buildList(ArrayList<WorksOn> empProjects)
    {   
        String[] projects = new String[empProjects.size()];
        int index = 0;
        for(WorksOn wobj : empProjects)
        {
        
            String name = wobj.getProject().getName();
            if(name.length() < 15){
                int length = name.length();
                while (length < 20)
               {
                  name = name + "  ";
                  length++;
               }
            }
            projects[index] =(name + "   " + wobj.getHours());
            index++;
        }
        jList1.setListData(projects);
    }
    
    
